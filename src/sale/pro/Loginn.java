    @SuppressWarnings({"deprecation"})
    //login func
    public void userlogin(String username, String password) throws SQLException
    {
              String uss = username;
               
             
                 Statement s = db.mycon().createStatement();
             
                 ResultSet rs = s.executeQuery("SELECT * FROM user WHERE Username = '"+username+"' AND Password = '"+password+"'");
                
                 if(rs.next())
                 {     s.close();
                      rs.close();
                       act_user(uss);
                        
                      JOptionPane.showMessageDialog(this, username+" Logged In Successfully");
                  
                        Home hm = new Home();
                       hm.jLabel4.setText(us_name.getText()+",");
                       hm.jLabel10.setText(us_name.getText());
                        hm.setVisible(true);
                        dispose(); 
                        
                     }
                     
                     else {
                          JOptionPane.showMessageDialog(this, "USERNAME / PASSWORD NOT FOUND","Error",JOptionPane.ERROR_MESSAGE);
                 }       
             
              
    
     }
    
