    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = em_name.getText();
        String username = us_name.getText();
        String password = String.valueOf(pass.getPassword());
        String con_pass = c_pass.getText();

        if(name.isEmpty() || username.isEmpty() || password.isEmpty() || con_pass.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Fill the missing details", "Error", JOptionPane.ERROR_MESSAGE);
        }
        //pass confirmation
        else if(con_pass.equals(password))
        {
            try {
                Statement s = (Statement) db.mycon().createStatement();
                s.executeUpdate(" INSERT INTO user (name,Username,Password) VALUES ('"+name+"','"+username+"','"+password+"')");
                 s.close();
                JOptionPane.showMessageDialog(this, "User Successfully Registered.");
                 s.close();
            } catch (SQLException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
          

        }

    }//GEN-LAST:event_jButton1ActionPerformed

